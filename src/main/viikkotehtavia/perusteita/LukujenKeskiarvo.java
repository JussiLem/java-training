package main.viikkotehtavia.perusteita;
import java.lang.*;

public class LukujenKeskiarvo {


    public static void main(String[] args) {
        int eka = 9;
        int toka = 7;
        int kolmas = 6;
        System.out.println("eka: " + eka);
        System.out.println("toka: " + toka);
        System.out.println("kolmas: " + kolmas);

        double d = 1.0 * (eka + toka + kolmas) / 3;
        // Kirjoita keskiarvon laskenta tämän kommentin alapuolelle
        System.out.println("keskiarvo: " + d);

    }
}
